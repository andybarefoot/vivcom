                <h2>How to Sponsor a child</h2>
                <p>You become a member of our family of sponsors and we will send you regular school reports, photos and letters from a child whom you sponsor. Sponsorship commitments are made on termly or annually and at the end of your sponsorship year you will be given the opportunity to continue to support your child as he or she continues to progress.</p>
                <h2>How sponsorship works:</h2>
                <p>It ensures that a child receives an education, clothing, nutritious food, health care, vocational training, life skills and most importantly a place to call home.</p>
                <ul>
