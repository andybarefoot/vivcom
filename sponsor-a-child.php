                <h2>Health Support</h2>
                <p>In addition to school fees and essential materials, Our Sponsorship Program helps support the medical needs of students, especially those under 11 years old. Medication and regular checkups are funded and provided by our organization through support by the sponsorship. The organization is responsible for taking the child to a health centre with quality services and pays all fees.</p>
