                <h2>Executive Summary:</h2>
                <p>Household and family vulnerability levels in Uganda expose children to high risks of malnutrition, homelessness and severe impoverishment. Such households cannot provide basic needs to family members and the children, such as education, proper shelter or even feed their children. These families therefore thrive in destitute and insolvent circumstances.</p>
                <p>The vulnerability levels in Ugandan households occur in threefold, symbolic measures manifest themselves in insurmountable rates and Critically Vulnerable households exhaust traits such as orphaned households, Families with children infected and affected with HIV, Child Headed Households and Children with Disability. Moderately vulnerable households exemplify traits such as elderly headed households, impoverished families as well as families in fishing communities, generally vulnerable households include single headed households where parents separated or divorced, households in bad/poor housing facilities, polygamous households and households in slum communities.</p>
