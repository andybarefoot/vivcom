                <p>This program is designed to offer our community the requisite hands on skills that can enable them transit from a life of insolvent and felony to a productive occupation. Here we came up with a list of programs as seen below:</p>
                <h2>Arts & Local Crafts</h2>
                <p>We design art and crafts to enable the community to gain valuable technical skills as well as expand their creative horizons. We inspire our artists to critically think, introspect and solve problems through creative designs that appeal to the everyday urban dweller. We teach our artists to appreciate the bond between art and culture as well as inspire them to explore new art styles.</p>
                <h2>Catering & Food Services</h2>
                <p>Catering and food services is taught using a variety of ingredients and equipment in order to learn the skills needed to produce food in a commercial environment. We emphasize personal and food hygiene, health and safety and as well as customer care essential for the hospitality and catering industry.</p>
                <h2>Computer Literacy & Graphic Design</h2>
                <p>We equip our community with basic computer applications including MS. Word, Excel, PowerPoint as well as internet and webmail. This package is usually given as a preparatory module for learners taking the graphic design course and includes understanding the principles conceptually and learning how to apply them.</p>
                <h2>Hairdressing & Cosmetology</h2>
                <p>We teach the community art and beauty. It has a variety of modules including manicure and pedicure, hair styling, cutting and color, making up which also covers eyebrows and lushes as well as ear piercing.</p>
                <h2>Electronics Maintenance and Repair</h2>
                <p>We enable participants to diagnose and repair system level faults in basic electronics systems (radios, computers, televisions e.t.c) at foundation level. At the end of the series practice, participants should be able to demonstrate a practical understanding of basic electronic systems and independently undertake basic repairs.</p>
                <h2>Tailoring & Fashion Design</h2>
                <p>The Tailoring and fashion design practical sessions are intended to support learners appreciate the use of modern sewing machines for commercial purposes. The training is practical and uses African fabrics helping learners gain hands on skills of making garments for the local market including dresses, shirts, trousers and skirts among others. Some participants opt to further gain additional training and skills in fashion and design.</p>
                <h2>Photography and Videography</h2>
                <p>In still life photography, we offer hands on skills to learners including how to use the latest cameras and produce high definition pictures. We also expose the students to the various mediums of photography including portrait, wildlife or landscape as well as teach them how to use light and shadows during a photo shoot. Video editing on the other hand covers the basics and is taught using a final cut pro software and includes story and editing, Final Cut Basics, how to import and conduct editing.</p>
